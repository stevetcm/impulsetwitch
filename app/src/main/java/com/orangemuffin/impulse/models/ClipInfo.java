public class ClipInfo {
    private String clipSlug;
    private String displayName;
    private String logoUrl;
    private String clipGameName;
    private String viewCount;
    private String clipTitle;
    private String clipLength;
    private String clipPreviewUrl;
    private String clipDate;
    private String vodId;
    private String vodOffset;

    public String getClipSlug() {
        return clipSlug;
    }

    public void setClipSlug(String clipSlug) {
        this.clipSlug = clipSlug;
    }

    public String getDisplayName() {
        return displayName;
    }

    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }

    public String getLogoUrl() {
        return logoUrl;
    }

    public void setLogoUrl(String logoUrl) {
        this.logoUrl = logoUrl;
    }

    public String getClipGameName() {
        return clipGameName;
    }

    public void setClipGameName(String clipGameName) {
        this.clipGameName = clipGameName;
    }

    public String getViewCount() {
        return viewCount;
    }

    public void setViewCount(String viewCount) {
        this.viewCount = viewCount;
    }

    public String getClipTitle() {
        return clipTitle;
    }

    public void setClipTitle(String clipTitle) {
        this.clipTitle = clipTitle;
    }

    public String getClipLength() {
        return clipLength;
    }

    public void setClipLength(String clipLength) {
        this.clipLength = clipLength;
    }

    public String getClipPreviewUrl() {
        return clipPreviewUrl;
    }

    public void setClipPreviewUrl(String clipPreviewUrl) {
        this.clipPreviewUrl = clipPreviewUrl;
    }

    public String getClipDate() {
        return clipDate;
    }

    public void setClipDate(String clipDate) {
        this.clipDate = clipDate;
    }

    public String getVodId() {
        return vodId;
    }

    public void setVodId(String vodId) {
        this.vodId = vodId;
    }

    public String getVodOffset() {
        return vodOffset;
    }

    public void setVodOffset(String vodOffset) {
        this.vodOffset = vodOffset;
    }
}
